CREATE VIEW [dbo].[VIdeaValidateLocation]
AS
SELECT     I.Id AS IdIdea, I.Code, I.Name, I.FSCContribution, I.Cost, LI.IdDepto, LI.IdCity
FROM         dbo.Idea AS I LEFT OUTER JOIN
                      dbo.LocationByIdea AS LI ON I.Code = LI.IdIdea LEFT OUTER JOIN
                      dbo.ThirdByIdea AS TI ON I.Code = TI.IdIdea
WHERE     (LI.IdDepto IS NULL) AND (LI.IdCity IS NULL)

